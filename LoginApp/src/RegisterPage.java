    private void RegisterButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_RegisterButtonActionPerformed
    String username = usernameField.getText().trim();
    String password = new String(passwordField.getPassword());
    String confirmPassword = new String(confirmPasswordField.getPassword()); // ðŸ‘ˆ new field

    // Check empty fields
    if (username.isEmpty() || password.isEmpty() || confirmPassword.isEmpty()) {
        javax.swing.JOptionPane.showMessageDialog(this, "Please fill all fields.");
        return;
    }

    // Check if passwords match
    if (!password.equals(confirmPassword)) {
        javax.swing.JOptionPane.showMessageDialog(this, "Passwords do not match. Try again.");
        return;
    }

    try (java.sql.Connection conn = Database.getConnection()) {
        // Check if username already exists
        String checkSql = "SELECT * FROM users WHERE username=?";
        java.sql.PreparedStatement checkStmt = conn.prepareStatement(checkSql);
        checkStmt.setString(1, username);
        java.sql.ResultSet rs = checkStmt.executeQuery();

        if (rs.next()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Username already exists. Please choose another.");
        } else {
            // Insert new user
            String insertSql = "INSERT INTO users(username, password) VALUES(?, ?)";
            java.sql.PreparedStatement insertStmt = conn.prepareStatement(insertSql);
            insertStmt.setString(1, username);
            insertStmt.setString(2, password);
            insertStmt.executeUpdate();

            javax.swing.JOptionPane.showMessageDialog(this, "Registration Successful! Please login.");

            this.dispose(); // Close RegisterForm
            new LoginForm().setVisible(true); // Open LoginForm
        }
    } catch (Exception e) {
        javax.swing.JOptionPane.showMessageDialog(this, "Database error: " + e.getMessage());
    }
    }//GEN-LAST:event_RegisterButtonActionPerformed

